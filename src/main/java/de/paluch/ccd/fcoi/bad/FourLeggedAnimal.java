public class FourLeggedAnimal extends Animal {

    private int leg1;
    private int leg2;
    private int leg3;
    private int leg4;

    /**
     * @return the leg1
     */
    public int getLeg1() {
        return leg1;
    }

    /**
     * @param leg1
     *            the leg1 to set
     */
    public void setLeg1(int leg1) {
        this.leg1 = leg1;
    }

    /**
     * @return the leg2
     */
    public int getLeg2() {
        return leg2;
    }

    /**
     * @param leg2
     *            the leg2 to set
     */
    public void setLeg2(int leg2) {
        this.leg2 = leg2;
    }

    /**
     * @return the leg3
     */
    public int getLeg3() {
        return leg3;
    }

    /**
     * @param leg3
     *            the leg3 to set
     */
    public void setLeg3(int leg3) {
        this.leg3 = leg3;
    }

    /**
     * @return the leg4
     */
    public int getLeg4() {
        return leg4;
    }

    /**
     * @param leg4
     *            the leg4 to set
     */
    public void setLeg4(int leg4) {
        this.leg4 = leg4;
    }

}
